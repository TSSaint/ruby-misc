# Ruby variables

num = 1
newNum = num + num
puts newNum
# results in 2

# we can use shorthand for multiplication, division, addition && subraction
count = 10 # set var 1
count += 10 # same as count + 10
count -= 5 # same as count - 1
count /= 3 # same as count / 2
count *= 100 # same as count * 100
puts count # returns 500

# vars are useful for making sens of and storing data
number = 13
is_true = number % 2 == 0 # is number's remainder when divided by 2 equal to zero
puts is_true # prints false

